# 1.1 Python Program for Arithmetic Operations
# This program accepts two numbers from the user and performs arithmetic operations on them. The user is asked to enter two numbers and then the program displays the sum, difference, product, and quotient of the two numbers. The program uses the input() function to accept the numbers from the user and then performs the arithmetic operations using the +, -, *, and / operators. The results are displayed using the print() function.

# Accept two numbers from the user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Addition
sum = num1 + num2

# Subtraction
diff = num1 - num2

# Multiplication
product = num1 * num2

# Division
quotient = num1 / num2

# Display the results
print("Sum:", sum)
print("Difference:", diff)
print("Product:", product)
print("Quotient:", quotient)


# Output
# Enter first number: 10
# Enter second number: 5
# Sum: 15.0
# Difference: 5.0
# Product: 50.0
# Quotient: 2.0

# In this program, we accept two numbers from the user using the input() function. The numbers are converted to floating-point numbers using the float() function. We then perform arithmetic operations on the numbers using the +, -, *, and / operators. The results are stored in variables sum, diff, product, and quotient. Finally, we display the results using the print() function.

